	@FXML
	private Button viewButton;

	// Event Listener on Button[#deleteButton].onAction
	@FXML
	public void deleteButtonOnAction(ActionEvent event) throws SQLException, IOException {
		// TODO Autogenerated
		this.getOwnerBuilder().getHostCompositionFunctionBuilder().deleteOriginalIndieFIVTypeEntryBuilder(this.getOwnerBuilder().getIndependentFreeInputVariableTypeBuilder().getCurrentValue().getName());
		
	}
	
