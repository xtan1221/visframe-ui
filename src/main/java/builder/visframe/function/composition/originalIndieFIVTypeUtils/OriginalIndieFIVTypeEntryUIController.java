	// Event Listener on Button[#viewButton].onAction
	@FXML
	public void viewButtonOnAction(ActionEvent event) throws SQLException, IOException {
		this.getOwnerBuilder().getIndependentFreeInputVariableTypeBuilder().setModifiable(false);
		
		this.getOwnerBuilder().getIndependentFreeInputVariableTypeBuilder().getIntegrativeUIController().showAndWait(this.getRootContainerNode().getScene().getWindow(), "Original IndependentFreeInputVariableType");
		
	}

